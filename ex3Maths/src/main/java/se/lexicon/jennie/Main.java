package se.lexicon.jennie;

public class Main {
    public static void main(String[] args) {

        int a = 45;
        int b = 11;
        int c = 12;
        int d = 4;
        int e = 24;
        int f = 6;
        int g = 55;



        System.out.println(a + "+" + b + " = " + (a+b));
        System.out.println(c + "*" + d + " = " + (c*d));
        System.out.println(e + "/" + f + " = " + (e/f));
        System.out.println(g + "-" + c + " = " + (g-c));

    }
}